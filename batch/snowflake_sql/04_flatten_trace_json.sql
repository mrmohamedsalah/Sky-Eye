CREATE TABLE IF NOT EXISTS AIRLINES_DB.BRONZE.FLIGHT_TRACES
(
    ICAO VARCHAR,
    REGISTRATION VARCHAR,
    AIRCRAFT_TYPE VARCHAR,
    DESCRIPTION VARCHAR,
    DB_FLAGS NUMBER,
    DATA_DATE DATE,
    EVENT_TIMESTAMP TIMESTAMP_NTZ,
    LAT FLOAT,
    LON FLOAT,
    ALT_BARO VARCHAR,
    ALT_GEOM FLOAT,
    GROUND_SPEED FLOAT,
    TRACK FLOAT,
    VERT_RATE FLOAT,
    GEOM_VERT_RATE FLOAT,
    IAS FLOAT,
    ROLL FLOAT,
    TRACE_FLAGS NUMBER,
    IS_STALE BOOLEAN,
    IS_NEW_LEG BOOLEAN,
    IS_GEOM_RATE BOOLEAN,
    IS_GEOM_ALT BOOLEAN,
    POSITION_TYPE VARCHAR,
    FLIGHT VARCHAR,
    SQUAWK VARCHAR,
    EMERGENCY VARCHAR,
    CATEGORY VARCHAR,
    NAV_ALTITUDE_MCP FLOAT,
    NAV_ALTITUDE_FMS FLOAT,
    NAV_HEADING FLOAT,
    NAV_QNH FLOAT,
    NIC NUMBER,
    RC FLOAT,
    NAC_P NUMBER,
    NAC_V NUMBER,
    SIL NUMBER,
    SIL_TYPE VARCHAR,
    GVA NUMBER,
    SDA NUMBER,
    VERSION NUMBER,
    SOURCE_FILE VARCHAR,
    INGESTION_TS TIMESTAMP_NTZ
);

INSERT INTO AIRLINES_DB.BRONZE.FLIGHT_TRACES
SELECT
    r.RAW_JSON:icao::VARCHAR AS ICAO,
    r.RAW_JSON:r::VARCHAR AS REGISTRATION,
    r.RAW_JSON:t::VARCHAR AS AIRCRAFT_TYPE,
    r.RAW_JSON:desc::VARCHAR AS DESCRIPTION,
    r.RAW_JSON:dbFlags::INTEGER AS DB_FLAGS,

    r.DATA_DATE AS DATA_DATE,
    DATEADD(
        'second',
        t.value[0]::FLOAT,
        TO_TIMESTAMP_NTZ(r.RAW_JSON:timestamp::NUMBER, 0)
    ) AS EVENT_TIMESTAMP,

    t.value[1]::FLOAT AS LAT,
    t.value[2]::FLOAT AS LON,
    t.value[3]::VARCHAR AS ALT_BARO,
    t.value[10]::FLOAT AS ALT_GEOM,

    t.value[4]::FLOAT AS GROUND_SPEED,
    t.value[5]::FLOAT AS TRACK,
    t.value[7]::FLOAT AS VERT_RATE,
    t.value[11]::FLOAT AS GEOM_VERT_RATE,
    t.value[12]::FLOAT AS IAS,
    t.value[13]::FLOAT AS ROLL,

    t.value[6]::INTEGER AS TRACE_FLAGS,
    BITAND(t.value[6]::INTEGER, 1) > 0 AS IS_STALE,
    BITAND(t.value[6]::INTEGER, 2) > 0 AS IS_NEW_LEG,
    BITAND(t.value[6]::INTEGER, 4) > 0 AS IS_GEOM_RATE,
    BITAND(t.value[6]::INTEGER, 8) > 0 AS IS_GEOM_ALT,

    t.value[9]::VARCHAR AS POSITION_TYPE,

    t.value[8]:flight::VARCHAR AS FLIGHT,
    t.value[8]:squawk::VARCHAR AS SQUAWK,
    t.value[8]:emergency::VARCHAR AS EMERGENCY,
    t.value[8]:category::VARCHAR AS CATEGORY,
    t.value[8]:nav_altitude_mcp::FLOAT AS NAV_ALTITUDE_MCP,
    t.value[8]:nav_altitude_fms::FLOAT AS NAV_ALTITUDE_FMS,
    t.value[8]:nav_heading::FLOAT AS NAV_HEADING,
    t.value[8]:nav_qnh::FLOAT AS NAV_QNH,
    t.value[8]:nic::INTEGER AS NIC,
    t.value[8]:rc::FLOAT AS RC,
    t.value[8]:nac_p::INTEGER AS NAC_P,
    t.value[8]:nac_v::INTEGER AS NAC_V,
    t.value[8]:sil::INTEGER AS SIL,
    t.value[8]:sil_type::VARCHAR AS SIL_TYPE,
    t.value[8]:gva::INTEGER AS GVA,
    t.value[8]:sda::INTEGER AS SDA,
    t.value[8]:version::INTEGER AS VERSION,

    r.SOURCE_FILE AS SOURCE_FILE,
    r.INGESTION_TS AS INGESTION_TS

FROM AIRLINES_DB.BRONZE.FLIGHT_TRACES_RAW r,
LATERAL FLATTEN(input => r.RAW_JSON:trace) t;
